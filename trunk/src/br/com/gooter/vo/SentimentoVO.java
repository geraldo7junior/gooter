public class SentimentoVO {

	private int idSentimento;
	private String valorSentimento;

	public int getIdSentimento() {
		return idSentimento;
	}

	public void setIdSentimento(int idSentimento) {
		this.idSentimento = idSentimento;
	}

	public String getValorSentimento() {
		return valorSentimento;
	}

	public void setValorSentimento(String valorSentimento) {
		this.valorSentimento = valorSentimento;
	}

}
